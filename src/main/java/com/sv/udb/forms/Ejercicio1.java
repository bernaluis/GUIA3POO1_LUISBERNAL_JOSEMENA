    private void btnPrimerosTresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrimerosTresActionPerformed
        //Los 3 androides con más vida
        jlistListados.removeAll();
        List<Androide> androides = obj.obtenerAndroide();
        DefaultListModel<Androide> model = new DefaultListModel<>();
        for (Androide android : androides) {
            if (android.getVida() > 50) {
                model.addElement(android);
                if (model.getSize() == 3) {
                    break;
                }
            }
        }
        jlistListados.setModel(model);      
    }//GEN-LAST:event_btnPrimerosTresActionPerformed

