    private void btnTareaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTareaActionPerformed
        Androide androide = new Androide();
        androide = jlstAndroides.getSelectedValue();
        //Asignar tarea a un androide
        switch (cmbTareas.getSelectedIndex()) {
            case 0:
                androide.setVida(androide.getVida() - 25);
                lblPorcentaje.setText(String.valueOf(androide.getVida()) + "%");
                break;
            case 1:
                androide.setVida(androide.getVida() - 50);
                lblPorcentaje.setText(String.valueOf(androide.getVida()) + "%");
                break;
            case 2:
                androide.setVida(androide.getVida() - 30);
                lblPorcentaje.setText(String.valueOf(androide.getVida()) + "%");
                break;
            case 3:
                androide.setVida(androide.getVida() - 25);
                lblPorcentaje.setText(String.valueOf(androide.getVida()) + "%");
                break;
            case 4:
                androide.setVida(androide.getVida() - 15);
                lblPorcentaje.setText(String.valueOf(androide.getVida()) + "%");
                break;
        }
        btnTarea.setEnabled(false);
    }//GEN-LAST:event_btnTareaActionPerformed

