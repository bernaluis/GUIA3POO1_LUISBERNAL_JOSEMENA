    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        //Agregar androide
        String nombre = txtAndroide.getText();
        int vida = 100;
        try {
            if (!"".equals(txtAndroide.getText())) {
                Androide androide = new Androide();
                androide.setsNombre(nombre);
                obj.agregarAndroide(androide, vida);
                JOptionPane.showMessageDialog(this, "Ha ingresado un nuevo androide", "Exito", JOptionPane.INFORMATION_MESSAGE);
                txtAndroide.setText("");
                lista(obj.obtenerAndroide());
            } else {
                JOptionPane.showMessageDialog(this, "Debe ingresar el nombre del Androide", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (HeadlessException ex) {
            System.err.println("Error inesperado al guardar " + ex.getMessage());
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

